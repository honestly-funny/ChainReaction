public class Position {
    public int x;
    public int y;
    public int numBalls;
    public int maxBalls;

    Position(int x_coord, int y_coord, int balls, int _maxBalls){
        x = x_coord;
        y = y_coord;
        numBalls = balls;
        maxBalls = _maxBalls;
    }


}
